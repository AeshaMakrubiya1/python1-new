'''
write a python program that uses at least five built-in-function (e.g., len(), max(), sorted(), sum(), and type() to perform operations on a list of numbers.
'''

# List of numbers
numbers = [12, 5, 8, 21, 7, 3]

# 1. type() – Check the data type of the variable
print("Type of 'numbers':", type(numbers))

# 2. len() – Get the number of elements in the list
print("Number of elements:", len(numbers))

# 3. max() – Find the largest number in the list
print("Maximum value:", max(numbers))

# 4. sum() – Calculate the total of all numbers
print("Sum of all numbers:", sum(numbers))

# 5. sorted() – Return a sorted version of the list
print("Sorted list:", sorted(numbers))
# perform addition, subtraction, multiplication, division, floor division, modulus, and exponentiation on two numbers input by the user.

a = float(input("Enter 1st number : "))
b = float(input("Enter 2nd number : ")) 

print("Addition :", a + b)
print("Subtraction :", a - b)
print("Multiplication :", a * b)
print("Division :", a / b)
print("Floor Division :", a // b)
print("Modulus :", a % b)
print("Exponentiation :", a ** b)
